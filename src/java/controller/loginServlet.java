    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String username = request.getParameter("username");
        String password = request.getParameter("password");

      
        
        System.out.println("username: " + username);
        System.out.println("password: " + password);

        // TODO: Xử lý xác thực đăng nhập ở đây
        // Simple null check and trimming
        if (username != null) {
            username = username.trim();
        }
        if (password != null) {
            password = password.trim();
        }
        // Initialize an empty error message
        String errorMessage = "";
        // Check if the credentials are correct
        if ("admin".equals(username) && "123456".equals(password)) {
            // Login success, redirect to form_fill.jsp
            response.sendRedirect("StudioRental.jsp");
        } else {
            // Login failed, set error message
            errorMessage = "WRONG USERNAME OR PASSWORD!";
            request.setAttribute("errorMessage", errorMessage);

            // Use forward only if the response has not been committed
            if (!response.isCommitted()) {
                request.getRequestDispatcher("Login.jsp").forward(request, response);
            } else {
                // Redirect if the response is already committed
                response.sendRedirect("Login.jsp");
            }
        }
    }

